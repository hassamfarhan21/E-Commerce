<?php
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Get the form fields
    $name = $_POST["name"];
    $email = $_POST["email"];
    $subject = $_POST["subject"];
    $message = $_POST["message"];
    
    // Set the recipient email address
    $to = "hassamkhanhk28@gmail.com.com"; // Replace with your recipient's email address
    
    // Set additional headers
    $headers = "From: $email\r\n";
    $headers .= "Reply-To: $email\r\n";
    $headers .= "Content-Type: text/plain; charset=utf-8\r\n";
    
    // Build the email content
    $email_body = "Name: $name\n";
    $email_body .= "Email: $email\n";
    $email_body .= "Subject: $subject\n";
    $email_body .= "Message:\n$message";
    
    // Send the email
    if (mail($to, $subject, $email_body, $headers)) {
        // Email sent successfully
        echo '<div id="success">Your message has been sent successfully.</div>';
    } else {
        // Email sending failed
        echo '<div id="success">Sorry, there was an error sending your message. Please try again later.</div>';
